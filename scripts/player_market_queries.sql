SELECT COUNT(*) FROM FIFA_PLAYERS;

SELECT SHORT_NAME, LONG_NAME, CLUB_NAME, NATIONALITY_NAME, OVERALL
FROM FIFA_PLAYERS
WHERE FIFA_EDITION = 22
ORDER BY OVERALL DESC
FETCH FIRST 10 ROWS ONLY;

SELECT NATIONALITY_NAME, COUNT(*) AS PLAYER_COUNT
FROM FIFA_PLAYERS
WHERE FIFA_EDITION = 22
AND OVERALL >= 85
GROUP BY NATIONALITY_NAME
ORDER BY PLAYER_COUNT DESC
FETCH FIRST 10 ROWS ONLY;

SELECT SHORT_NAME, CLUB_NAME, NATIONALITY_NAME,PLAYER_POSITIONS, OVERALL, WAGE_EUR, VALUE_EUR
FROM FIFA_PLAYERS
WHERE FIFA_EDITION = 22
AND OVERALL >= 85
AND WAGE_EUR > 0
AND PLAYER_POSITIONS NOT LIKE '%GK%'
ORDER BY WAGE_EUR ASC
FETCH FIRST 10 ROWS ONLY;

SELECT 
  CASE 
    WHEN PLAYER_POSITIONS LIKE '%GK%' THEN 'Goalkeeper'
    WHEN PLAYER_POSITIONS LIKE '%CB%' OR PLAYER_POSITIONS LIKE '%RB%' OR PLAYER_POSITIONS LIKE '%LB%' THEN 'Defender'
    WHEN PLAYER_POSITIONS LIKE '%CM%' OR PLAYER_POSITIONS LIKE '%CAM%' OR PLAYER_POSITIONS LIKE '%CDM%' THEN 'Midfielder'
    WHEN PLAYER_POSITIONS LIKE '%ST%' OR PLAYER_POSITIONS LIKE '%CF%' OR PLAYER_POSITIONS LIKE '%LW%' OR PLAYER_POSITIONS LIKE '%RW%' THEN 'Forward'
    ELSE 'Other'
  END AS POSITION_GROUP,
  COUNT(*) AS PLAYER_COUNT,
  AVG(OVERALL) AS AVG_OVERALL,
  AVG(PACE) AS AVG_PACE,
  AVG(SHOOTING) AS AVG_SHOOTING,
  AVG(DEFENDING) AS AVG_DEFENDING
FROM FIFA_PLAYERS
WHERE FIFA_EDITION = 22
GROUP BY 
  CASE 
    WHEN PLAYER_POSITIONS LIKE '%GK%' THEN 'Goalkeeper'
    WHEN PLAYER_POSITIONS LIKE '%CB%' OR PLAYER_POSITIONS LIKE '%RB%' OR PLAYER_POSITIONS LIKE '%LB%' THEN 'Defender'
    WHEN PLAYER_POSITIONS LIKE '%CM%' OR PLAYER_POSITIONS LIKE '%CAM%' OR PLAYER_POSITIONS LIKE '%CDM%' THEN 'Midfielder'
    WHEN PLAYER_POSITIONS LIKE '%ST%' OR PLAYER_POSITIONS LIKE '%CF%' OR PLAYER_POSITIONS LIKE '%LW%' OR PLAYER_POSITIONS LIKE '%RW%' THEN 'Forward'
    ELSE 'Other'
  END
ORDER BY AVG_OVERALL DESC;

SELECT CLUB_NAME, COUNT(*) AS SQUAD_SIZE, 
       AVG(OVERALL) AS AVG_RATING,
       MAX(OVERALL) AS BEST_PLAYER_RATING
FROM FIFA_PLAYERS
WHERE FIFA_EDITION = 22
AND CLUB_NAME != 'No Club'
GROUP BY CLUB_NAME
HAVING COUNT(*) >= 11
ORDER BY AVG_RATING DESC
FETCH FIRST 10 ROWS ONLY;

SELECT SHORT_NAME, FIFA_EDITION, OVERALL
FROM FIFA_PLAYERS
WHERE SHORT_NAME IN ('L. Messi', 'Cristiano Ronaldo', 'L. Suárez', 'R. Lewandowski', 'K. De Bruyne')
AND FIFA_EDITION IN (15, 16, 17, 18, 19, 20, 21, 22)
ORDER BY FIFA_EDITION, OVERALL DESC;

SELECT SHORT_NAME, LONG_NAME, FIFA_EDITION, OVERALL
FROM FIFA_PLAYERS
WHERE (SHORT_NAME, LONG_NAME) IN (
    SELECT SHORT_NAME, LONG_NAME
    FROM FIFA_PLAYERS
    GROUP BY SHORT_NAME, LONG_NAME
    ORDER BY AVG(OVERALL) DESC
    FETCH FIRST 5 ROWS ONLY
)
ORDER BY FIFA_EDITION;

SELECT LEAGUE_NAME, AVG(OVERALL) AS AVERAGE_RATING, FIFA_EDITION
FROM FIFA_PLAYERS 
WHERE LEAGUE_NAME IN ('Spain Primera Division', 'English Premier League')
GROUP BY LEAGUE_NAME, FIFA_EDITION
HAVING COUNT(*) >= 100
ORDER BY AVG(OVERALL) DESC;

