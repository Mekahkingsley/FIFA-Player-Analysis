CASE 
 WHEN PLAYER_POSITIONS LIKE '%GK%' THEN 'Goalkeeper'
    WHEN PLAYER_POSITIONS LIKE '%CB%' OR PLAYER_POSITIONS LIKE '%RB%' OR PLAYER_POSITIONS LIKE '%LB%' THEN 'Defender'
    WHEN PLAYER_POSITIONS LIKE '%CM%' OR PLAYER_POSITIONS LIKE '%CAM%' OR PLAYER_POSITIONS LIKE '%CDM%' THEN 'Midfielder'
    WHEN PLAYER_POSITIONS LIKE '%ST%' OR PLAYER_POSITIONS LIKE '%CF%' OR PLAYER_POSITIONS LIKE '%LW%' OR PLAYER_POSITIONS LIKE '%RW%' THEN 'Forward'
    ELSE 'Other'
  END AS POSITION_GROUP,
  AVG(AGE) AS AVERAGE_AGE, FIFA_EDITION
  FROM FIFA_PLAYERS
  GROUP BY FIFA_EDITION,
  CASE 
 WHEN PLAYER_POSITIONS LIKE '%GK%' THEN 'Goalkeeper'
    WHEN PLAYER_POSITIONS LIKE '%CB%' OR PLAYER_POSITIONS LIKE '%RB%' OR PLAYER_POSITIONS LIKE '%LB%' THEN 'Defender'
    WHEN PLAYER_POSITIONS LIKE '%CM%' OR PLAYER_POSITIONS LIKE '%CAM%' OR PLAYER_POSITIONS LIKE '%CDM%' THEN 'Midfielder'
    WHEN PLAYER_POSITIONS LIKE '%ST%' OR PLAYER_POSITIONS LIKE '%CF%' OR PLAYER_POSITIONS LIKE '%LW%' OR PLAYER_POSITIONS LIKE '%RW%' THEN 'Forward'
    ELSE 'Other'
  END
  ORDER BY FIFA_EDITION, AVG(age) ;
  
  SELECT NATIONALITY_NAME, COUNT(DISTINCT SOFIFA_ID) AS PLAYERS
FROM FIFA_PLAYERS
GROUP BY NATIONALITY_NAME
ORDER BY COUNT(DISTINCT SOFIFA_ID) DESC
FETCH FIRST 10 ROWS ONLY;

SELECT COUNT(
CASE 
WHEN POTENTIAL > OVERALL THEN 1
END) * 100 / COUNT(*) AS POTENTIAL_PERCENTAGE, fifa_edition, AVG(POTENTIAL - OVERALL) AS AVERAGE_DIFFRENCE
FROM FIFA_PLAYERS
GROUP BY FIFA_EDITION
ORDER BY FIFA_EDITION;

SELECT SOFIFA_ID, MIN(FIFA_EDITION) AS FIRST_EDITION, 
MAX(FIFA_EDITION) AS LAST_EDITION
FROM FIFA_PLAYERS
GROUP BY SOFIFA_ID;

SELECT F2.SHORT_NAME, F2.NATIONALITY_NAME,
       F2.OVERALL AS FIRST_RATING,
       F3.OVERALL AS LAST_RATING,
       F3.OVERALL - F2.OVERALL AS RATING_IMPROVEMENT
FROM 
    (SELECT SOFIFA_ID, 
            MIN(FIFA_EDITION) AS FIRST_EDITION, 
            MAX(FIFA_EDITION) AS LAST_EDITION
     FROM FIFA_PLAYERS
     GROUP BY SOFIFA_ID
     HAVING COUNT(DISTINCT FIFA_EDITION) > 1) F1
JOIN FIFA_PLAYERS F2 
    ON F1.SOFIFA_ID = F2.SOFIFA_ID 
    AND F2.FIFA_EDITION = F1.FIRST_EDITION
JOIN FIFA_PLAYERS F3 
    ON F1.SOFIFA_ID = F3.SOFIFA_ID 
    AND F3.FIFA_EDITION = F1.LAST_EDITION
ORDER BY RATING_IMPROVEMENT DESC
FETCH FIRST 10 ROWS ONLY
